        static void DisplayWelcomeMessage()
        {
            Console.ForegroundColor = ConsoleColor.Green;
            Console.WriteLine("=========================================================================================");
            Console.WriteLine("Hello! Welcome to the Cybersecurity Awareness Bot. I'm here to help you stay safe online.");
            Console.WriteLine("=========================================================================================");
            Console.ResetColor();
        }

        static void PlayVoiceGreeting()
        {
            try
            {
                if (File.Exists(WELCOME_AUDIO_FILE))
                {
                    using (var player = new SoundPlayer(WELCOME_AUDIO_FILE))
                    {
                        player.Play();
                        // Wait for the audio to finish playing
                        Thread.Sleep(4000);
                    }
                }
                else
                {
                    Console.ForegroundColor = ConsoleColor.Yellow;
                    Console.WriteLine("Note: Welcome audio file not found");
                    Console.ResetColor();
                }
            }
            catch (Exception ex)
            {
                Console.ForegroundColor = ConsoleColor.Red;
                Console.WriteLine($"Audio Error: {ex.Message}");
                Console.ResetColor();
            }
        }
        static string GetUserName()
        {
            string userName;
            do
            {
                Console.ForegroundColor = USER_COLOR;
                Console.Write("\nPlease enter your name to begin: ");
                Console.ForegroundColor = ConsoleColor.White;
                userName = Console.ReadLine()?.Trim();

                if (string.IsNullOrEmpty(userName))
                {
                    Console.ForegroundColor = ConsoleColor.Yellow;
                    Console.WriteLine("Please enter a valid name");
                }

            } while (string.IsNullOrEmpty(userName));

            return userName;
        }

        static void DisplayPersonalizedWelcome(string userName)
        {
            Console.Clear();
            DisplayAsciiArt();

            Console.ForegroundColor = USER_COLOR;
            TypeWriteEffect($"Welcome, {userName}!", TYPEWRITER_DELAY);

            DrawBorder();
            TypeWriteEffect("I'm your cybersecurity assistant here to help you:", TYPEWRITER_DELAY);
            Console.WriteLine("- Stay safe online");
            Console.WriteLine("- Recognize security threats");
            Console.WriteLine("- Protect your digital identity");
            DrawBorder();

            Console.ResetColor();
        }

        static void DrawBorder()
        {
            Console.ForegroundColor = BORDER_COLOR;
            Console.WriteLine("==================================================================");
            Console.ResetColor();
        }

        static void TypeWriteEffect(string text, int delay)
        {
            foreach (char c in text)
            {
                Console.Write(c);
                Thread.Sleep(delay);
            }
            Console.WriteLine();
        }
